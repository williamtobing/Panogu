          <p>Ketika kita pertama kali mendengar kata public speaking. Apa yang terlintas di pikiran kita? Sudah jelas kemampuan berbicara bukan?, kemampuan berkomunikasi di hadapan banyak orang. Nah bagi anda yang datang ke website ini kemudian membaca artikel ini, entah disengaja atau tidak disengaja, sebelumnya saya ingin mengucapkan terimakasih karena sudah meluangkan waktunya untuk membaca tulisan ini, disini saya akan mengajak anda semua untuk belajar bersama-sama tentang <em>cara belajar public speaking</em> secara otodidak.</p>
          <h2>Belajar public speaking otodidak, Bisakah?</h2>
          <p>Ilmu public speaking merupakan soft skill yang banyak memberikan kontribusi bagi setiap orang. Kembali lagi pada prosesnya public speaking bukanlah soft skill yang bisa dikuasai dengan mudah oleh semua orang. Kadang ada orang yang mudah menguasai public speaking, namun kadang ada juga orang yang sulit untuk menguasai public speaking.</p>
          <p>Terlepas dari semua itu anda jangan khawatir, public speaking bisa di pelajari oleh siapapun. walau mungkin di awal bagi sebagian orang sangat sulit, namun percayalah anda bisa menguasainya.</p>
          <p>Di dalam tulisan ini saya ingin mengajak anda untuk belajar public speaking secara otodidak atau belajar sendiri di rumah dan di lingkungan anda sehari-hari.</p>
          <h2>Bagaimana cara belajar public speaking secara otodidak? Berikut adalah tipsnya!</h2>
          <p><strong>1. Biasakan setiap pagi belajar berbicara</strong></p>
          <p>Pertama kali saya belajar public speaking, saya masih ingat pada waktu itu hampir setiap pagi saya membiasakan diri saya untuk latihan berbicara. Biasanya kalau mau mandi sebelum masuk ke kamar mandi saya berbicara dulu, entah itu membayangkan diri sebagai seorang pemimpin dan lain sebagainya. Mungkin aktivitas semacam ini terdengar aneh bagi anda, namun percayalah ini adalah hal yang biasa bagi seorang profesional public speaker. Mereka juga latihan, sering beajar, tanpa latihan seseorang tidak akan pernah bisa memberikan hasil yang maksimal. Jadi jangan mengira orang yang pandai dalam public speaking emang dari dulu udah pandai begitu, melainkan mereka juga latihan dan sering belajar.</p>
          <p><strong>2. Praktik di dalam kehidupan sehari-hari</strong></p>
          <p>Poin terpenting dalam sebuah pembelajaran adalah praktik. Dalam belajar public speaking kalau anda hanya belajar teori saja tanpa anda pernah praktik ya sama saja ga ada gunanya. Orang yang pandai teori belum tentu pandai praktik dan orang yang selalu mempraktikan ilmu tentu akan pandai dalam memberikan teori.</p>
          <p>Jadi, cobalah mempraktikan ilmu yang anda pelajari di dalam kehidupan sekitar anda, entah itu dilingkungan sekolah, kerja dan lain sebagainya.</p>
          <p><strong>3. Lakukan evaluasi secara terus menerus</strong></p>
          <p>Selain anda terus-menerus mempraktikan ilmu anda, hal yang juga perlu anda lakukan adalah mengevaluasi diri. Evaluasilah setiap kekurangan-kekurangan yang ada di dalam diri sebelumnya.</p>
          <p>Pertamakali anda belajar public speaking pasti terlihat kaku atau bahkan tidak menarik untuk di lihat dan di dengar audiens, hal ini wajar terjadi sebab seorang profesional public speaker pun juga tidak mungkin langsung pandai dalam komunikasi, mereka juga berproses, pernah mengalami masa-masa seperti itu.</p>
          <p>Bila anda rutin mengevaluasi diri, sering bertanya kepada teman atau kerabat tentang bagaimana perkembangan public speaking anda, saya yakin dalam waktu yang tidak lama skill public speaking anda akan menjadi lebih baik.</p>
          <p>Jadi itulah beberapa tips tentang &#8220;Cara Belajar Public Speaking Otodidak dengan Mudah.</p>
